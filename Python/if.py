# If condition
a = 7
b = 8
if a < b:
    print(a, "is smaller than", b)


# If Else condition
a = 5
b = 4
if a < b:
    print(a, "is smaller than", b)
else:
    print(b, "is smaller than", a)

# Elif statement
a = 7
b = 8
if a > b:
    print(a, "is larger than", b)
elif b >= a:
    print(b, "is larger than", a)
