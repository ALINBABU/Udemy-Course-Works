# While loop statement
i = 1
while i < 7:
    print(i)
    i += 1

print("\n")

# For loop statement
fruits = ["Grapes", "Berries", "Oranges"]
for fruit in fruits:
    print(fruit)

# Nested for loop statement
colors = ["Green", "Yellow", "Purple"]
fruits = ["Apples", "Banana", "Berries"]
for color in colors:
    for fruit in fruits:
        print(color, fruit)
